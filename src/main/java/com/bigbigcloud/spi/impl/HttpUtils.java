package com.bigbigcloud.spi.impl;

import org.apache.http.HttpEntity;
import org.apache.http.NameValuePair;
import org.apache.http.ParseException;
import org.apache.http.client.methods.CloseableHttpResponse;
import org.apache.http.client.methods.HttpGet;
import org.apache.http.client.utils.URIBuilder;
import org.apache.http.impl.client.CloseableHttpClient;
import org.apache.http.impl.client.HttpClients;
import org.apache.http.util.EntityUtils;
import java.io.IOException;
import java.net.URI;
import java.net.URISyntaxException;
import java.util.List;
import static com.bigbigcloud.BrokerConstants.*;

public final class HttpUtils {
    private static final CloseableHttpClient httpclient = HttpClients.createDefault();

    public static URI doGetHttpUrl(String host, String path, List<NameValuePair> nvps) {
        URI uri = null;
        try {
            uri = new URIBuilder().setScheme("http").setHost(host)
                    .setPath(path).addParameters(nvps).build();
        } catch (URISyntaxException e) {
            e.printStackTrace();
        }
        return uri;
    }

    public static String sendGet(URI uri) {
        HttpGet httpget = new HttpGet(uri);
        httpget.setHeader(AUTHSIGNATURE, BEARER);
        CloseableHttpResponse response = null;
        try {
            response = httpclient.execute(httpget);
        } catch (IOException e1) {
            e1.printStackTrace();
        }
        String result = null;
        try {
            HttpEntity entity = response != null ? response.getEntity() : null;
            if (entity != null) {
                result = EntityUtils.toString(entity);
            }
        } catch (ParseException | IOException e) {
            e.printStackTrace();
        } finally {
            try {
                if (response != null) {
                    response.close();
                }
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
        return result;
    }
}
