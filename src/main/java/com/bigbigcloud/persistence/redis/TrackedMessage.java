package com.bigbigcloud.persistence.redis;

import java.io.Serializable;
import java.util.Date;

public class TrackedMessage implements Serializable {

    private static final long serialVersionUID = 1755296138639817304L;
    private MessageStatus messageStatus;
    private int count;
    private long timeStamp;

    TrackedMessage() {
    }

    public TrackedMessage(MessageStatus messageStatus) {
        this.messageStatus = messageStatus;
        this.count = 0;
        this.timeStamp = new Date().getTime();
    }

    public MessageStatus getMessageStatus() {
        return messageStatus;
    }

    public void setMessageStatus(MessageStatus messageStatus) {
        this.messageStatus = messageStatus;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public long getTimeStamp() {
        return timeStamp;
    }

    public void setTimeStamp(long timeStamp) {
        this.timeStamp = timeStamp;
    }
}
